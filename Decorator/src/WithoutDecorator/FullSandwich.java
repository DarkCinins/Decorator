package WithoutDecorator;

public class FullSandwich
{
    int breadCentimeters;
    int cheeseSlices;
    String cheeseType;
    int hamburguerQuantity;
    String hamburguerType;
    String saladType;

    public int getBreadCentimeters() {
        return breadCentimeters;
    }

    public void setBreadCentimeters(int breadCentimeters) {
        this.breadCentimeters = breadCentimeters;
    }

    public int getCheeseSlices() {
        return cheeseSlices;
    }

    public void setCheeseSlices(int cheeseSlices) {
        this.cheeseSlices = cheeseSlices;
    }

    public String getCheeseType() {
        return cheeseType;
    }

    public void setCheeseType(String cheeseType) {
        this.cheeseType = cheeseType;
    }

    public int getHamburguerQuantity() {
        return hamburguerQuantity;
    }

    public void setHamburguerQuantity(int hamburguerQuantity) {
        this.hamburguerQuantity = hamburguerQuantity;
    }

    public String getHamburguerType() {
        return hamburguerType;
    }

    public void setHamburguerType(String hamburguerType) {
        this.hamburguerType = hamburguerType;
    }

    public String getSaladType() {
        return saladType;
    }

    public void setSaladType(String saladType) {
        this.saladType = saladType;
    }
}